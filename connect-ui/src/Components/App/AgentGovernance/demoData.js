function scaleCostData(baseDeployments, periodDays) {
  const factor = periodDays / 30;
  const jitter = (idx) => 0.85 + ((idx * 17 + periodDays * 3) % 30) / 100;
  return baseDeployments.map((d, i) => {
    const f = factor * jitter(i);
    return {
      ...d,
      inputTokens: Math.round(d.inputTokens * f),
      outputTokens: Math.round(d.outputTokens * f),
      totalTokens: Math.round(d.totalTokens * f),
      requestCount: Math.round(d.requestCount * f),
      inputCost: Math.round(d.inputCost * f * 100) / 100,
      outputCost: Math.round(d.outputCost * f * 100) / 100,
      totalCost: Math.round(d.totalCost * f * 100) / 100,
    };
  });
}

export function getDemoAzureCost(periodDays = 7) {
  const deployments = scaleCostData(DEMO_AZURE_COST_BASE.deployments, periodDays);
  return {
    summary: {
      totalCost: deployments.reduce((s, d) => s + d.totalCost, 0),
      totalTokens: deployments.reduce((s, d) => s + d.totalTokens, 0),
      totalRequests: deployments.reduce((s, d) => s + d.requestCount, 0),
    },
    deployments,
  };
}

export function getDemoGoogleCost(periodDays = 7) {
  const endpoints = scaleCostData(DEMO_GOOGLE_COST_BASE.endpoints, periodDays);
  return {
    summary: {
      totalCost: endpoints.reduce((s, d) => s + d.totalCost, 0),
      totalTokens: endpoints.reduce((s, d) => s + d.totalTokens, 0),
      totalPredictions: endpoints.reduce((s, d) => s + d.requestCount, 0),
    },
    endpoints,
  };
}

