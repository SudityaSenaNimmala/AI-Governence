export const formatDateNew = (newDate, isHex) => {
  let isHexo = isHex;
  if (newDate?.toString().length >= 12) {
    isHexo = true;
  } else {
    isHexo = false;
  }
  let date = new Date(isHexo ? newDate : newDate * 1000);
  const year = date.getFullYear();
  const month = (date.getMonth() + 1).toString().padStart(2, "0");
  const day = date.getDate().toString().padStart(2, "0");
  return `${month}/${day}/${year}`;
};

